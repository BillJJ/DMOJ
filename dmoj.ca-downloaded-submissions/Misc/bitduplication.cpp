unsigned long long duplicatebits(unsigned long long l){
    l = (l<<16 | l) & 0b0000000000000000111111111111111100000000000000001111111111111111;
    l = (l<<8 | l) & 0b0000000011111111000000001111111100000000111111110000000011111111;
    l = (l<<4 | l) & 0b0000111100001111000011110000111100001111000011110000111100001111;
    l = (l<<2 | l) & 0b0011001100110011001100110011001100110011001100110011001100110011;
    l = (l<<1 | l) & 0b0101010101010101010101010101010101010101010101010101010101010101;
    return (l<<1 | l);
}